def calculate_intuition_index(player_value: int, all_values: list[int]) -> int:
    sorted_vals = sorted(all_values)
    rank = sorted_vals.index(player_value) + 1
    return round((rank / len(all_values)) * 100)


def calculate_potential_value(player_box_value: int) -> int:
    return player_box_value


def assign_title(
    intuition_index: int,
    accepted_offer: bool,
    counter_offered: bool,
    counter_accepted: bool,
    rounds_played: int,
) -> str:
    went_all_way = not accepted_offer

    # 隐藏成就：直觉满分（选中 $1M 箱子）
    if intuition_index == 100:
        return "百万传奇" if went_all_way else "最贵的遗憾"

    # 1. 天启交易师：顶级直觉 + 坚持到底
    if intuition_index >= 88 and went_all_way:
        return "天启交易师"
    # 2. 谈判艺术家：还价被接受
    if counter_accepted:
        return "谈判艺术家"
    # 3. 意志砥柱：高直觉 + 坚持到底
    if intuition_index >= 75 and went_all_way:
        return "意志砥柱"
    # 4. 精准猎手：高直觉 + 第2-4轮成交
    if intuition_index >= 70 and accepted_offer and 1 <= rounds_played <= 3:
        return "精准猎手"
    # 5. 直觉信徒：高直觉 + 中期成交（不与精准猎手重叠）
    if intuition_index >= 80 and accepted_offer and 1 <= rounds_played <= 4:
        return "直觉信徒"
    # 6. 闪电决断者：第1轮立即成交
    if accepted_offer and rounds_played == 0:
        return "闪电决断者"
    # 7. 卧龙待醒：中高直觉 + 第5轮以后才成交
    if intuition_index >= 55 and accepted_offer and rounds_played >= 4:
        return "卧龙待醒"
    # 8. 戏剧制造者：还价失败后接受原报价
    if counter_offered and not counter_accepted and accepted_offer:
        return "戏剧制造者"
    # 9. 赌徒哲学家：低直觉 + 坚持到底
    if intuition_index < 40 and went_all_way:
        return "赌徒哲学家"
    # 10. 孤胆英雄：中等直觉 + 坚持到底
    if went_all_way:
        return "孤胆英雄"
    # 11. 淡然成交者：低直觉 + 早期成交
    if intuition_index < 55 and accepted_offer and rounds_played <= 2:
        return "淡然成交者"
    # 12. 随遇而安者：兜底
    return "随遇而安者"


def get_title_info(title_name: str) -> dict:
    return TITLES.get(title_name, TITLES["随遇而安者"])
